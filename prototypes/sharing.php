<p>You can send your friends and family this URL and they can use it to look at your photo online, from anywhere in the world. By doing this, you're sharing your photo.</p>

<p>Anybody can link to any page on the web &mdash; you don't need permission to do it.</p>

<p>You can share other people's photos or posts, and anyone can share yours. The people who see those links might then share it with even more people.</p>

<p>Sometimes a post will be shared onwards by thousands, occasionally millions, of people. This is what's happened when something goes viral.</p>

<h2>How do I share my photo?</h2>

<p class='standfirst'>URLs are often long and complicated, like the example above &mdash; but sharing them is simple. Two of the easiest ways are manually and with share buttons.</p>

<h3>Manually</h3>

<p>You can send a URL directly in an email, through a messaging service (e.g. iMessage or Skype), or by posting it on a social media site. A URL sent this way is often referred to as a link.</p>

<p>To share a URL as a link, copy it in full from the address bar and paste it straight into the email or message you're sending, or onto the site you've chosen.</p>

<p>Anyone receiving the link in your email or message, or seeing it on the site, will be able to click on it and go directly to the webpage that your Weather Watchers photo sits on.</p>

