<h1>What can go wrong?</h1>

Sometimes, there can be problems with security:

<b>Identity:</b> Somebody might pretend to be you and make an account in your name. This is very rare, tending only to happen to people in the public eye.

<b>Authentication:</b> Somebody could get hold of your password and access your identity. This is why you should use a different passwords on every account, and never write passwords down. A password manager like 1Pass or LastPass is really helpful here.

<b>Authorisation:</b> You're able to do things on a site that you should be blocked from. This could be because there are bugs in the software that its engineers need to fix. Alternatively, someone might have given you too many permissions.

