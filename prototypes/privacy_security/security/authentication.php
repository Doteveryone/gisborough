<h1>Authentication: you are who you say you are</h1>

<p class='standfirst'>A website will often verify that you are who you say, before allowing you access to your account.</p>

<p>You can think of this as the website providing the window lock, with you as the user bringing the key. If your key doesn't fit, you won't be able to enter.</p>

<p>The most familiar method of authentication for websites is a password. Phones often have passcodes. Some devices now even use biometric identification, like thumb-print unlocking on iPads.</p>

<p>Two-factor authentication adds an extra layer of checking. The person logging in needs to know the password and have access to a second device which is used to verify the password. Often it's a text message sent to a phone, or it can be a dedicated device like a bank secure key.</p>

