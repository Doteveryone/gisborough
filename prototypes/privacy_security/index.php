<h1>Security and Privacy</h1>

Security and privacy on the internet are sometimes thought of as being the same thing. In reality, they're quite different.

Security is like a lock on a window of your house. You can decide to fit a lock, and who has a key. This controls access to your house.

Privacy is like a curtain you hang at the window. You can choose when to open or close it, and what you're doing when it's open. The curtain controls what you keep secret in your house.

Understanding this difference can help you see which elements of your internet presence you have control over, and how you can control your information.

