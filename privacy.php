<h2>Privacy</h2>
<p class='standfirst'>Privacy on the internet isn't something to automatically worry about – it's something to think about.</p>

<p>As in your offline life, you decide what you're comfortable showing or talking about publicly online. Choices about privacy are very often concern what you want to keep secret, and who you want to keep it secret from.</p> 

<p>These choices – about what you show, to whom, how, where and when – are personal and in your control. Once you make them, you can use settings on websites and apps to apply them to what you show and share.</p>

<h2>Things to think about</h2>

<h3>What you show</h3>

<p class='standfirst'>How much are you comfortable with others knowing or seeing of your life – what you think and do, who you spend time with, where you go?</p>

<p>Consider people: friends, family, acquaintances, teammates; colleagues, bosses, employees. Does what you share depend on, what you do, when you do it, or who you're with? Are there things you don't want to show or say of your free time, work time, family time?</p>

<p>Consider events: weddings, birthdays and holidays; nights out and sports matches; conferences and leaving parties. Does what you keep private online match what you keep private offline? What different things do you do in different contexts that might alter what you share? What might people think about you sharing events that involve them?</p>

<p>Consider topics: morals, ethics, religion, politics, personal experiences. Would you express the same views online to a wider audience as you would offline to a small group? Might you feel more open with a distant audience? What implications might expressing your views online have, beyond just making your point?</p>

<h3>Who you show it to</h3>

<p class='standfirst'>Do you want to limit which people can see what you think and do? If so, who do you feel comfortable seeing? Friends and family? Friends of your friends too? Anyone anywhere in the world?</p>

<p>Perhaps who can see changes what you want to show – you have control over what you put out there.</p>

<h3>How you show it</h3>

<p class='standfirst'>You can choose how closely tied your real identity is to your online accounts.</p>

<p>Are you using your real name? Are you completely anonymous? Are you using a pseudonym, like a nickname or handle? Can people work out who you are from it? Does that matter to you or in your wider life?</p>

<h3>Where you show it</h3>

<p class='standfirst'>All of the different sites and apps give you different functions and settings for showing information about yourself and your activities.</p>

<p>Explore the options on the sites and apps you use or are interested in using. Which have more of the settings you want? Which are you more comfortable with for what purpose?</p>

<p>Remember: you always have the option of not using a service – the easiest way to keep something private is to not share it anywhere.</p>

<h3>When you show it</h3>

<p class='standfirst'>Life changes over time, so time can also change your choices about your online privacy.</p>

<p>Say you have children, take on a high-profile role, or move to a new town. You might choose to share more or less, or in a different way, depending on your social circle or work environment.</p>

<p>It's a good idea to reconsider your privacy choices from time to time, and check you're still comfortable with what you're showing.</p>

<div class='boxout'>
  <h2>Privacy breaches</h2>
  <div class='inner'>
    <p>People often associate privacy breaches with deliberate attacks. But privacy is about people's choices, so privacy breaches are often down to human error.</p>

    <h3>Mistakes</h3>

    <p>A common mistake is being unaware of things being publicly visible. This is usually because a user hasn't switched the settings on a site or app to match what they want, giving more people authorisation to view things.</p>

    <p><a href="https://identity.utexas.edu/everyone/how-to-manage-your-social-media-privacy-settings">How to check your settings on social media.</a></p>

    <p>A common oversight is having one account identity inadvertently connect to another. For example, someone might use their real name and nickname on one site, but just a nickname on another. Googling could help someone link both accounts back to the real name.</p>

    <p>Something commonly forgotten is what you share about other people. For example, if they're in photos with you, or you mention them in a post. Your choices can affect, and maybe breach, other people's privacy, just as theirs can yours. If you're in doubt, ask before you post.</p>

    <h3>Lack of permission</h3>

    <p>Other people's content isn't yours to use without their permission, and vice versa.</p>

    <p>Anyone can link to a Weather Watcher's photo without permission, but it shouldn't be copied for another use without their permission.</p>

    <p>Some sites and apps have terms and conditions on the use of members' content. Copyright law also applies, just as it does to offline creative outputs.</p>

    <p>There are exceptions for <a href="https://en.wikipedia.org/wiki/Creative_Commons"Creative Commons</a> and public domain content. If you're in doubt, don't copy.</a></p>

    <h3>Poor software</h3>

    <p>Poorly designed software can make it unclear what's public and what's private. Settings and functions might be complicated to use, or it might be difficult to check what's visible to whom.</p>

    <p>Badly made software could have bugs that unintentionally reveal information to the wrong users. These are often caught and fixed, but not always.</p>
