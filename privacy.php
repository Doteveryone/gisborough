  <h1>Security and Privacy</h1>
</div>

<p class='standfirst'>Security and privacy on the internet are sometimes thought of as being the same thing. In reality, they're quite different.</p>

<p>Security is like a lock on a window of your house. You can decide to fit a lock, and who has a key. This controls <em>access</em> to your house.</p>

<p>Privacy is like a curtain you hang at the window. You can choose when to open or close it, and what you're doing when it's open. The curtain controls what you keep <em>secret</em> in your house.</p>

<p>Understanding this difference can help you see which elements of your internet presence you have control over, and how you can control your information.</p>

<h2>Access security</h2>

<p class='standfirst'>Access security is about personal security on the accounts, profiles and logins you have for websites or apps. There are three aspects: identity, authentication, and authorisation.</p>

<h3>Identity: <em>who you are</em></h3>
<p class='standfirst'>Different websites will ask for different combinations of identifying information to create an account identity. This might include email address, username or nickname, real name, postal address, and postcode.</p> 

<p>For example, the Weather Watchers site asks you to create a nickname to display alongside your photo, rather than using your real name.</p>

<p>Your identity is unique within each website. You can't usually have two accounts on the same site, which are tied to the same email address. But you might use the same identifying information on different websites.</p>

<h3>Authentication: <em>you are who you say you are</em></h3>

<p class='standfirst'>Before allowing access to your account, a website will often verify that you are who you say.</p>

<p>You can think of this as the website providing the window lock, with you as the user bringing the key. If the key doesn't fit, you won't be able to enter.</p>

<p>The most familiar method of authentication for websites is a password. Phones often have passcodes. Some devices now even use biometric identification, like thumb-print unlocking.</p>

<p>Two-factor authentication adds an extra layer of verification. The person logging in needs to know the password and have access to a second device which is used to verify the password. Often it's a text message sent to a phone, or it's a dedicated device like a bank secure key.</p>

<h3>Authorisation: <em>you're allowed to do the thing you want to do</em></h3>

<p class='standfirst'>Authorisation allows or limits what you can do on a website or device once it has authenticated your identity.</p>

<p>Some websites, like your bank's, won't let you do anything unless you're logged in.</p>

<p>Other websites will let you do some things without being logged in, but allow more once you do. For example, anyone can browse and filter items on eBay, but only logged-in users can bid on them.</p>

