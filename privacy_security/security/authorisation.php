<h1>Authorisation: you can do the thing you want to do</h1>

<p class='standfirst'>Authorisation allows or limits what you can do on a website or device once it's authenticated your identity.</p>

<p>Some websites won't let you do anything unless you're logged in.</p>

<p>Most websites will let you do some things without being logged in, but allow more once you do. For example, anyone can browse the products on a bank's website, but only logged-in users can arrange payments or transfers on their own accounts.</p>

<h3>What can go wrong?</h3>

<p>Very occasionally you might be able to do things on a site that you should be blocked from. This could be because there are mistakes or bugs in the software that its engineers need to fix. It might be that someone administrating the software gave you too many permissions. Or it could just be a friend may have chosen to share social media post with too wide a group.</p>

