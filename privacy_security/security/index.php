<p class='standfirst'>Security online is about limiting access to your logins and accounts. It can be thought of as having three aspects:</p> 

<ul>
  <li>
    <strong><a href='identity'>Identity</a></strong> &ndash; <em>who you are</em><br/>
    Telling websites and apps which user you are.
  </li>
  <li>
    <strong><a href='authentication'>Authentication</a></strong> &ndash; <em>you are who you say you are</em><br/>
    Verifying your identity with a password or code.
  </li>
  <li>
    <strong><a href='authorisation'>Authorisation</a></strong> &ndash; <em>you can do the thing you want to do</em><br/>
    Allowing or limiting your activity depending on who you are.
  </li>
</ul>

