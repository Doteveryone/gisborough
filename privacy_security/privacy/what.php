<h1>Privacy: what information you show</h1>

<p class='standfirst'>This aspect of privacy is about how much are you comfortable with others knowing or seeing of your life: what you think and do, who you spend time with, where you go.</p>

<p>Consider people: friends, family, acquaintances, teammates; colleagues, bosses, employees.</p>

<ul class='tightlist'>
  <li>Does what you share depend on what you do, when you do it, or who you’re with?</li>
  <li>Are there things you don’t want to show or say about your free time, work time, family time?</li>
  <li>Are there people who might not want you to show them or mention them?</li>
</ul>

<p>Consider events: weddings, birthdays and holidays; nights out and sports matches; conferences and leaving parties.</p>

<ul class='tightlist'>
  <li>Does what you keep private online match what you keep private offline?</li>
  <li>What different things do you do in different contexts that might alter what you share?</li>
  <li>What might people think about you sharing details of events that involve them?</li>
</ul>

<p>Consider topics: morals, ethics, religion, politics, personal experiences.</p>

<ul class='tightlist'>
  <li>Would you express the same views online, to a wider audience, as you would offline to a small group?</li>
  <li>Might you feel more open with a distant audience?</li>
  <li>What implications might expressing your views online have, beyond just making your point?</li>
</ul>

<p>Remember, just because you're thinking about all this, doesn't mean your friends and family are. Someone could share things about you &ndash; photos, attendance at events, news you told them alone &ndash; without considering whether it affects your privacy. If you see something that makes you uncomfortable, talk to them about it.</p>

